package com.goldwind.datalogic.business.model;

/**
 * 组态计算公式配置表
 * 
 * @author 冯春源
 *
 */
public class ConfigExpressInfo
{
    /**
     * 协议编号（前端交互时选择设备，底层保存时按照协议保存）
     */
    private String protocolid;

    /**
     * 虚拟iec路径
     */
    private String iecpath;

    /**
     * 虚拟iec类型（类似标准IEC的col_3）
     */
    private String iectype;

    /**
     * 描述
     */
    private String descr;

    /**
     * 计算公式
     */
    private String express;

    /**
     * 计算类型：1-四则运算，2-逻辑运算
     */
    private String expresstype;

    /**
     * 是否进行瞬态存储：0-不存储，1-存储
     */
    private String bsave;

    /**
     * 是否进行变位存储：0-不存储，1-存储
     */
    private String changesave;

    /**
     * 是否启用：0-不启用，1-启用
     */
    private String isenable;

    /**
     * 修改人
     */
    private String modifyuserid;

    /**
     * 修改时间
     */
    private String modifytime;

    /**
     * 是否删除（逻辑删除标志位）：0-未删除，1-已删除
     */
    private String delflg;

    /**
     * 系统id
     */
    private String systemid;

    /**
     * 分类编号，表示该告警代码所属二级分类
     */
    private String typeid;

    /**
     * 组态相关操作类型 0代表数据变化 1 数据属性变化 2 设备组属性变化
     */
    private String opertype;

    /**
     * 是否告警:0-不告警,1-告警
     */
    private String warnflag;
    


    public String getProtocolid()
    {
        return protocolid;
    }

    public void setProtocolid(String protocolid)
    {
        this.protocolid = protocolid;
    }

    public String getIecpath()
    {
        return iecpath;
    }

    public void setIecpath(String iecpath)
    {
        this.iecpath = iecpath;
    }

    public String getIectype()
    {
        return iectype;
    }

    public void setIectype(String iectype)
    {
        this.iectype = iectype;
    }

    public String getDescr()
    {
        return descr;
    }

    public void setDescr(String descr)
    {
        this.descr = descr;
    }

    public String getExpress()
    {
        return express;
    }

    public void setExpress(String express)
    {
        this.express = express;
    }

    public String getExpresstype()
    {
        return expresstype;
    }

    public void setExpresstype(String expresstype)
    {
        this.expresstype = expresstype;
    }

    public String getBsave()
    {
        return bsave;
    }

    public void setBsave(String bsave)
    {
        this.bsave = bsave;
    }

    public String getChangesave()
    {
        return changesave;
    }

    public void setChangesave(String changesave)
    {
        this.changesave = changesave;
    }

    public String getIsenable()
    {
        return isenable;
    }

    public void setIsenable(String isenable)
    {
        this.isenable = isenable;
    }

    public String getModifyuserid()
    {
        return modifyuserid;
    }

    public void setModifyuserid(String modifyuserid)
    {
        this.modifyuserid = modifyuserid;
    }

    public String getModifytime()
    {
        return modifytime;
    }

    public void setModifytime(String modifytime)
    {
        this.modifytime = modifytime;
    }

    public String getDelflg()
    {
        return delflg;
    }

    public void setDelflg(String delflg)
    {
        this.delflg = delflg;
    }

    public String getSystemid()
    {
        return systemid;
    }

    public void setSystemid(String systemid)
    {
        this.systemid = systemid;
    }

    public String getTypeid()
    {
        return typeid;
    }

    public void setTypeid(String typeid)
    {
        this.typeid = typeid;
    }

    public String getWarnflag()
    {
        return warnflag;
    }

    public void setWarnflag(String warnflag)
    {
        this.warnflag = warnflag;
    }

    public String getOpertype()
    {
        return opertype;
    }

    public void setOpertype(String opertype)
    {
        this.opertype = opertype;
    }

    public ConfigExpressInfo() 
    {
        
    }
    
    public ConfigExpressInfo(String protocolid, String iecpath, String iectype, String descr, String express, String expresstype, String bsave, String changesave, String isenable, String modifyuserid,
            String modifytime, String delflg, String systemid, String typeid, String warnflag, String opertype) 
    {
        this.protocolid = protocolid;
        this.iecpath = iecpath;
        this.iectype = iectype;
        this.descr = descr;
        this.express = express;
        this.expresstype = expresstype;
        this.bsave = bsave;
        this.changesave = changesave;
        this.isenable = isenable;
        this.modifyuserid = modifyuserid;
        this.modifytime = modifytime;
        this.delflg = delflg;
        this.systemid = systemid;
        this.typeid = typeid;
        this.warnflag = warnflag;
        this.opertype = opertype;
    }
}
