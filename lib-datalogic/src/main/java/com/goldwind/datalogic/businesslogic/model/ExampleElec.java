package com.goldwind.datalogic.businesslogic.model;

/**
 * 增加对象
 * 
 * @author 谭璟
 *
 */
public class ExampleElec
{
    /**
     * 时间
     */
    private String rectime;

    /**
     * 损失电量
     */
    private double elec;

    /**
     * 个数
     */
    private int num;

    public ExampleElec()
    {

    }

    /**
     * 带参数构造函数
     * 
     * @param rectime
     * @param elec
     * @param num
     */
    public ExampleElec(String rectime, double elec, int num)
    {
        this.rectime = rectime;
        this.elec = elec;
        this.num = num;
    }

    public String getRectime()
    {
        return rectime;
    }

    public void setRectime(String rectime)
    {
        this.rectime = rectime;
    }

    public double getElec()
    {
        return elec;
    }

    public void setElec(double elec)
    {
        this.elec = elec;
    }

    public int getNum()
    {
        return num;
    }

    public void setNum(int num)
    {
        this.num = num;
    }
}
